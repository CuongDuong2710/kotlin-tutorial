fun main(args: Array<String>) {

    // casting double to int
    println("3.14 to Int: " + (3.14.toInt()))

    // casting char to int
    println("A to Int: " + ('A'.toInt()))

    // casting int to char
    println("65 to Char: " + (65.toChar()))
}